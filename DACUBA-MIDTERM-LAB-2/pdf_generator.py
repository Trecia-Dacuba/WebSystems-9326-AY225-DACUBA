from datetime import datetime

from reportlab.lib import colors
from reportlab.lib.pagesizes import A4
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import mm
from reportlab.lib.enums import TA_LEFT, TA_CENTER, TA_RIGHT
from reportlab.platypus import (
    SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle,
    HRFlowable, PageBreak, Preformatted
)
from reportlab.platypus import BaseDocTemplate, PageTemplate, Frame
from reportlab.pdfbase import pdfmetrics
from reportlab.pdfbase.ttfonts import TTFont

# ── Constants ─────────────────────────────────────────────────────────────────
OUTPUT_DIR = os.path.join(os.path.dirname(__file__), "output")
GENERATOR_NAME = "GFG Linux Academic Scraper"
SUBJECT_CATEGORY = "Linux Operating System — Technical Reference"

# Brand colours (GFG green palette + neutral academic grays)
GFG_GREEN  = colors.HexColor("#2F8D46")   # GFG primary green
GFG_DARK   = colors.HexColor("#1a5c2e")   # dark green for headings
LIGHT_BG   = colors.HexColor("#f0faf3")   # very light green tint
CODE_BG    = colors.HexColor("#1e1e1e")   # dark code background
CODE_FG    = colors.HexColor("#d4d4d4")   # light text for code
GRAY_LINE  = colors.HexColor("#cccccc")
BADGE_EASY   = colors.HexColor("#28a745")
BADGE_MEDIUM = colors.HexColor("#fd7e14")
BADGE_HARD   = colors.HexColor("#dc3545")
BADGE_NA     = colors.HexColor("#6c757d")

PAGE_W, PAGE_H = A4
LEFT_MARGIN = RIGHT_MARGIN = 18 * mm
TOP_MARGIN  = 22 * mm
BOT_MARGIN  = 22 * mm

