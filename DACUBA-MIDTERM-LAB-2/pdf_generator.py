"""
pdf_generator.py
----------------
Converts the list of scraped GFG article dicts into a professional
academic PDF that looks like a college instructional module.

Layout per article:
  ┌─────────────────────────────────────────────────────────────┐
  │  HEADER  — Subject Category | Date of Generation            │
  ├─────────────────────────────────────────────────────────────┤
  │  TOPIC TITLE  (large, highlighted)                          │
  │  Difficulty Level badge                                     │
  │  ─────────────────────────────────────────────────────────  │
  │  KEY TECHNICAL CONCEPTS                                     │
  │  …text…                                                     │
  │  CODE SNIPPETS / IMPLEMENTATION                             │
  │  …monospace code box…                                       │
  │  COMPLEXITY ANALYSIS                                        │
  │  …text…                                                     │
  │  REFERENCES / RELATED LINKS                                 │
  │  • link text (url)                                          │
  ├─────────────────────────────────────────────────────────────┤
  │  FOOTER  — Page N  |  "Generated by GFG Linux Scraper"     │
  └─────────────────────────────────────────────────────────────┘
"""

